struct MoveInstruction
{
    int amount;
    int from;
    int to;

    void Read(const std::string& instr)
    {
        sscanf(
            instr.c_str(),
            "move %d from %d to %d",
            &amount,
            &from,
            &to
        );
    }
};

std::string calcTopCrateLetters(
    const std::vector<std::string>& inputStacks,
    const std::vector<std::string>& inputMoves)
{
    // read the initial stack
    std::map<int,std::stack<char>> crates;
    std::vector<int> crateIndexes;
    for (int i=1; i<(int)inputStacks.rbegin()->size()-1; i+=4) {
        int index = int(inputStacks.rbegin()->at(i) - '0');
        crates[index] = std::stack<char>();
        crateIndexes.push_back(index);
    }
    for (int i=inputStacks.size()-2; i>=0; --i) {
        auto indexIter = crateIndexes.begin();
        for (int j=1; j<(int)inputStacks[i].size()-1; j+=4) {
            if (inputStacks[i][j] != ' ') {
                crates[*indexIter].push(inputStacks[i][j]);
            }
            ++indexIter;
        }
    }

    // Apply the stack operations
    std::vector<MoveInstruction> moves(inputMoves.size());
    for (size_t i=0; i<inputMoves.size(); ++i)
    {
        MoveInstruction& move = moves[i];
        move.Read(inputMoves[i]);
        std::stack<char>& fromStack = crates[move.from];
        std::stack<char>& toStack = crates[move.to];
        for (int i=0; i<move.amount; ++i)
        {
            assert(!fromStack.empty());
            toStack.push(fromStack.top());
            fromStack.pop();
        }
    }

    // Combine the top of the stacks for the final string
    std::string topCrates = "";
    for (auto pair : crates)
    {
        std::stack<char>& stk = pair.second;
        assert(!stk.empty());
        topCrates += stk.top();
    }
    return topCrates;
}

